package com.example.blitzware_android.navigation

/**
 * Navigation type
 *
 * @constructor Create empty Navigation type
 */
enum class NavigationType {
    /**
     * Bottom Navigation
     *
     * @constructor Create empty Bottom Navigation
     */
    BOTTOM_NAVIGATION,

    /**
     * Navigation Rail
     *
     * @constructor Create empty Navigation Rail
     */
    NAVIGATION_RAIL,

    /**
     * Permanent Navigation Drawer
     *
     * @constructor Create empty Permanent Navigation Drawer
     */
    PERMANENT_NAVIGATION_DRAWER
}